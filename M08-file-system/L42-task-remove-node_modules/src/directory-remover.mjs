import { rm } from 'node:fs/promises';
import path from 'node:path'

export async function directoryRemover(dirents = []) {
  for(const dirent of dirents) {
    if(dirent.isDirectory()) {
      const pathToRemove = path.join(dirent.path, dirent.name);
      try {
        await rm(pathToRemove, { recursive: true })
        console.log('Path', pathToRemove, 'was removed!')
      } catch (e) {
        console.error(e);
      }
    }
  }
}
